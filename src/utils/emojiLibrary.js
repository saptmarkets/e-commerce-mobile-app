// Helper function to get random emoji from a category
export const getRandomEmoji = (category) => {
  if (Array.isArray(category)) {
    return category[Math.floor(Math.random() * category.length)];
  }
  return category;
};

// Helper function to get emoji by product type
export const getProductEmoji = (productType) => {
  const type = productType.toLowerCase();
  
  if (type.includes('vegetable') || type.includes('produce')) {
    return getRandomEmoji(productEmojis.freshProduce.vegetables);
  }
  if (type.includes('fruit')) {
    return getRandomEmoji(productEmojis.freshProduce.fruits);
  }
  if (type.includes('dairy') || type.includes('milk')) {
    return getRandomEmoji(productEmojis.dairy.milk);
  }
  if (type.includes('meat') || type.includes('beef')) {
    return getRandomEmoji(productEmojis.meatFish.beef);
  }
  if (type.includes('fish') || type.includes('seafood')) {
    return getRandomEmoji(productEmojis.meatFish.fish);
  }
  if (type.includes('bread') || type.includes('bakery')) {
    return getRandomEmoji(productEmojis.grains.bread);
  }
  if (type.includes('beverage') || type.includes('drink')) {
    return getRandomEmoji(productEmojis.beverages.cold);
  }
  if (type.includes('snack')) {
    return getRandomEmoji(productEmojis.snacks.chips);
  }
  
  // Default
  return 'ðŸ›’';
};

